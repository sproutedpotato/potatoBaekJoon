lamda = int(input())

if 620 <= lamda <= 780:
  print("Red")
elif 590 <= lamda < 620:
  print("Orange")
elif 570 <= lamda < 590:
  print("Yellow")
elif 495 <= lamda < 570:
  print("Green")
elif 450 <= lamda < 495:
  print("Blue")
elif 425 <= lamda < 450:
  print("Indigo")
else:
  print("Violet")