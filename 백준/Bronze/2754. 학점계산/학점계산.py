arr = input()

if(arr == "A+"):
    print("4.3")
elif(arr == "A0"):
    print("4.0")
elif(arr == "A-"):
    print("3.7")
elif(arr == "B+"):
    print("3.3")
elif(arr == "B0"):
    print("3.0")
elif(arr == "B-"):
    print("2.7")
elif(arr == "C+"):
    print("2.3")
elif(arr == "C0"):
    print("2.0")
elif(arr == "C-"):
    print("1.7")
elif(arr == "D+"):
    print("1.3")
elif(arr == "D0"):
    print("1.0")
elif(arr == "D-"):
    print("0.7")
else:
    print("0.0")
